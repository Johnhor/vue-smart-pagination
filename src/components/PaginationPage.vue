        return 'loader-1'
      } else if (this.settings.spinnerStyle === 'spinnerStyle-2') {
        return 'loader-2'
      } else {
        return 'loader-3'
      }
    }
  },
  beforeMount () {
    let root = document.querySelector(':root')
    if (this.settings.spinnerColor) {
      root.style.setProperty('--spinnerColor', this.settings.spinnerColor)
    }
  }
}
</script>

